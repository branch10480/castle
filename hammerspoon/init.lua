-- Hammerspoon Configuration

-- ─────────────────────────────────────────────────────────────
-- 外部から Hammerspoon を制御するためのブリッジを有効化
-- ─────────────────────────────────────────────────────────────
-- - hs.ipc.cliInstall(): `hs -c "lua expr"` CLI ヘルパを使えるようにする
-- - hs.allowAppleScript: osascript 経由 (`tell application "Hammerspoon"
--   to execute lua code "..."`) で reload を発火できるようにする
-- どちらも初回 reload 後から有効になる。castle のメンテナンス時に
-- ターミナル側から Hammerspoon を操作できる経路を確保しておく。
require("hs.ipc")
if hs.ipc and hs.ipc.cliInstall then hs.ipc.cliInstall() end
hs.allowAppleScript(true)

-- Ctrl+SpaceでGhosttyにフォーカス（なければ起動）
local ghosttyBundleID = "com.mitchellh.ghostty"

hs.hotkey.bind({"ctrl"}, "space", function()
    local app = hs.application.get(ghosttyBundleID)
    if app then
        app:activate()
    else
        hs.application.launchOrFocusByBundleID(ghosttyBundleID)
    end
end)

-- ─────────────────────────────────────────────────────────────
-- Ghostty font-size を OS appearance に追従させる
-- ─────────────────────────────────────────────────────────────
-- castle のテーマ運用ルール「Light vs Dark = 1pt 差」を Ghostty で実現する。
-- Ghostty の theme file は font-size を silently ignore するため、本体
-- config の末尾で `config-file = ?config.local` として optional include
-- された machine-local ファイルを Hammerspoon が動的に書き換える方式を取る。
--
-- 動作:
--   1. AppleInterfaceThemeChangedNotification を distributedNotifications
--      で購読し、appearance 切替時に発火
--   2. defaults read で現在の interface style を判定 (Light / Dark)
--   3. ~/.config/ghostty/config.local を `font-size = 13` or `font-size = 14`
--      だけ書いた最小ファイルとして上書き
--   4. Hammerspoon 起動時にも 1 度同期 (cold start で正しい値に揃える)
--
-- 制約:
--   - Ghostty の font-size は live reload されないため、書き換え直後の
--     **既存ウィンドウ**は変わらない。次に開く新ウィンドウから新サイズ適用
--   - つまり「appearance を切り替えたら、Ghostty で新タブ/新ウィンドウを
--     開いた瞬間に新 font-size が効く」UX となる
--   - 全 window を即時更新したい場合は Ghostty を再起動する必要がある

local FONT_SIZE_LIGHT = 13
local FONT_SIZE_DARK = 14
local GHOSTTY_LOCAL_CONFIG = os.getenv("HOME") .. "/.config/ghostty/config.local"

-- defaults read -g AppleInterfaceStyle は Light モードでは "key not found"
-- エラー終了するため stderr を捨て、出力に "Dark" が含まれるかで判定する。
local function currentInterfaceStyle()
    local output = hs.execute("defaults read -g AppleInterfaceStyle 2>/dev/null", true)
    if output and output:match("Dark") then
        return "Dark"
    end
    return "Light"
end

local function syncGhosttyFontSize()
    local style = currentInterfaceStyle()
    local size = (style == "Dark") and FONT_SIZE_DARK or FONT_SIZE_LIGHT
    local f, err = io.open(GHOSTTY_LOCAL_CONFIG, "w")
    if not f then
        hs.printf("[ghostty-font] failed to open %s: %s", GHOSTTY_LOCAL_CONFIG, err)
        return
    end
    f:write("# auto-generated by hammerspoon/init.lua — do not edit manually\n")
    f:write(string.format("font-size = %d\n", size))
    f:close()
    hs.printf("[ghostty-font] wrote font-size = %d (style=%s)", size, style)
end

-- Hammerspoon 起動 (= cold start / hs.reload()) 時に 1 度同期
syncGhosttyFontSize()

-- macOS の appearance 変更通知を購読。グローバル変数に保持して GC で
-- watcher が破棄されないようにする (Hammerspoon の watcher 共通の罠)。
ghosttyAppearanceWatcher = hs.distributednotifications.new(function()
    syncGhosttyFontSize()
end, "AppleInterfaceThemeChangedNotification")
ghosttyAppearanceWatcher:start()
